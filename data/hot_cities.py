def build_domestic_seed_items() -> list[dict]:
    """从热门城市榜单生成国内种子（每城 2 个代表景点，不含境外）。"""
    items: list[dict] = []
    sid = 1
    for city, names in HOT_CITY_SCENICS.items():
        loc = CITY_FULL_LOCATION.get(city, f"{city}市")
        for idx, name in enumerate(names[:2]):
            items.append(
                {
                    "id": sid,
                    "name": name,
                    "category": "nature",
                    "region": "domestic",
                    "location": loc,
                    "price": 0.0,
                    "image": None,
                    "images": [],
                    "description": None,
                    "opening_hours": "以景区当日公告为准",
                    "best_season": "四季皆宜",
                    "tips": "出行前请在高德地图或景区官网核实开放时间、票价与预约政策。",
                    "latitude": None,
                    "longitude": None,
                    "tags": ["热门城市", city],
                    "is_hot": 1 if idx == 0 else 0,
                }
            )
            sid += 1
    return items
